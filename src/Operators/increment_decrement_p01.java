package Operators;

public class increment_decrement_p01 {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a);//11
        int a1 = 10;
        System.out.println(a1--);//10 //9
        int b = 20;
        System.out.println(++b);//21
        int b1 = 20;
        System.out.println(b1--); //19 //20
        int c = 30;
        System.out.println(--c); //29
        int d = 40;
        System.out.println(d++); //40
        System.out.println(d); //41
    }
}
